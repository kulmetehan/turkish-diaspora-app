// Helper function to get province by code
export function getProvinceByCode(code: string): TurkeyProvince | undefined {
  return TURKEY_PROVINCES.find((p) => p.code === code);
}

// Helper function to get province by name (Turkish or Dutch)
export function getProvinceByName(name: string): TurkeyProvince | undefined {
  return TURKEY_PROVINCES.find(
    (p) => p.name_tr.toLowerCase() === name.toLowerCase() || p.name_nl.toLowerCase() === name.toLowerCase()
  );
}
